#!/bin/bash
# ============================================================
# 日志预览脚本
# 使用方法: ./log.sh [行数]
# ============================================================

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
LOG_FILE="$SCRIPT_DIR/output.log"
LINES=${1:-50}

echo "============================================================"
echo "日志预览 (最后 $LINES 行)"
echo "文件: $LOG_FILE"
echo "============================================================"

if [ -f "$LOG_FILE" ]; then
    # 显示文件大小
    SIZE=$(du -h "$LOG_FILE" | cut -f1)
    echo "文件大小: $SIZE"
    echo "============================================================"
    echo ""
    
    # 显示最后 N 行
    tail -n $LINES "$LOG_FILE"
    
    echo ""
    echo "============================================================"
    echo "实时跟踪: tail -f $LOG_FILE"
    echo "============================================================"
else
    echo "日志文件不存在"
fi
